
package datatypeandvariable;


public class varex1 {
    public static void main(String[] args) {
        // var naming convention
        // var name not be any KEYWORD
        // var name should be start with letter
        // var name maybe with lower camel case ex myRollNo
        
        int a ,c;    //var declaration
        int b = 10;   //var doc with initialization
        a = 99; //   var initialization
        c = a+b;
        
        System.out.println("val of a "+a);
        System.out.println("val of b "+b);
        System.out.println("add of a and b is "+c);
        
    }
    
}
